#include "../ft_printf.h"

void	manage_f(char **format)
{
	if (**format != '\0')
	{
		(*format)++;
		while (**format == '-' || **format == '0')
		{
			if (**format == '0' && g_global.f != '-')
			{
				g_global.f = '0';
			}
			else
			{
				g_global.f = '-';
			}
			(*format)++;
		}
	}
}
